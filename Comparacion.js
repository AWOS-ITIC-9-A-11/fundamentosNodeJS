let num1 = 0;
let num2 = 0;

//Comparación simple: Sólo compara el valor, mas no el tipo de dato.
if (num1 == num2) {
    console.log('Son iguales');
} else {
    console.log('Son diferentes');
}

//Comparación estricta: Compara el valor y el tipo de dato
if (num1 === num2) {
    console.log('Son iguales');
} else {
    console.log('Son diferentes');
}